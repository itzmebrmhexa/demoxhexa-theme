/**
 * Icon replacement function
 * Automatically replaces icons in the DOM
 */
export function replaceIcons() {
  // Find all icon elements
  const iconElements = document.querySelectorAll('i[class*="fa-"]');
  
  iconElements.forEach(icon => {
    const classList = Array.from(icon.classList);
    
    // Check if icon needs replacement
    Object.entries(CYBER_ICONS).forEach(([key, value]) => {
      const oldIconClass = value.split(' ').pop(); // Get last class (icon name)
      
      if (classList.some(cls => cls.includes(oldIconClass.replace('fa-', '')))) {
        // Remove old classes
        icon.className = '';
        // Add new cyber icon classes
        icon.className = value;
      }
    });
  });
  
  console.log('[DEMO X HEXA] Icons replaced successfully');
}

/**
 * Get icon by name
 * @param {string} name - Icon name from CYBER_ICONS
 * @returns {string} - Font Awesome classes
 */
export function getIcon(name) {
  return CYBER_ICONS[name] || 'fa-solid fa-circle';
}

/**
 * Create icon element
 * @param {string} name - Icon name from CYBER_ICONS
 * @param {object} options - Additional options (size, color, etc.)
 * @returns {HTMLElement} - Icon element
 */
export function createIcon(name, options = {}) {
  const icon = document.createElement('i');
  icon.className = getIcon(name);
  
  if (options.size) icon.style.fontSize = options.size;
  if (options.color) icon.style.color = options.color;
  if (options.classes) icon.className += ' ' + options.classes;
  if (options.spin) icon.classList.add('fa-spin');
  if (options.pulse) icon.classList.add('fa-pulse');
  
  return icon;
}

/**
 * Initialize icon system
 * Call this after DOM is loaded
 */
export function initIcons() {
  // Replace existing icons
  replaceIcons();
  
  // Observe DOM changes and replace new icons
  const observer = new MutationObserver((mutations) => {
    mutations.forEach((mutation) => {
      if (mutation.addedNodes.length) {
        mutation.addedNodes.forEach((node) => {
          if (node.nodeType === 1) { // Element node
            const icons = node.querySelectorAll('i[class*="fa-"]');
            if (icons.length > 0) {
              replaceIcons();
            }
          }
        });
      }
    });
  });
  
  observer.observe(document.body, {
    childList: true,
    subtree: true
  });
  
  console.log('[DEMO X HEXA] Icon system initialized');
}

// Auto-initialize if DOM is ready
if (typeof document !== 'undefined') {
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initIcons);
  } else {
    initIcons();
  }
}

